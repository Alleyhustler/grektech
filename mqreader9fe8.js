function calcChecksum() {
	i = 0;
	var val = 6825;
	while (i < 32768) {
		var hexdata = getHexValue(i * 2, 2, false);
		var hexval = parseInt(hexdata, 16);
		
		if (i == 16) {
			hexval = 0;
		}
		
		val += hexval;
		val %= 65535;
		
		i += 1;
	}
	return 65535 - val;
	
}

